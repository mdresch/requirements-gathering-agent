// Process different file types
async function processFile(filePath) {
    const ext = path.extname(filePath).toLowerCase();
    const content = await fs.readFile(filePath, 'utf-8');
    
    let htmlContent = '';
    let title = path.basename(filePath, ext).replace(/[-_]/g, ' ').replace(/\b\w/g, l => l.toUpperCase());
    
    switch (ext) {
        case '.md':
            htmlContent = marked(content, {
                gfm: true,
                breaks: true,
                headerIds: false,
                mangle: false
            });
            break;
            
        case '.txt':
            htmlContent = content
                .split('\n')
                .map(line => {
                    const trimmed = line.trim();
                    return trimmed ? `<p>${trimmed}</p>` : '<p>&nbsp;</p>';
                })
                .join('\n');
            break;
            
        case '.html':
            return content; // Return as-is for HTML files
            
        default:
            throw new Error(`Unsupported file type: ${ext}`);
    }
    
    return createHTML(title, htmlContent, filePath);
}

// Convert single file to PDF
async function convertToPDF(inputPath, outputPath) {
    let browser = null;
    
    try {
        // Process content
        const htmlContent = await processFile(inputPath);
        
        // Launch Puppeteer
        browser = await puppeteer.launch({
            headless: 'new',
            args: [
                '--no-sandbox',
                '--disable-setuid-sandbox',
                '--disable-dev-shm-usage'
            ]
        });
        
        const page = await browser.newPage();
        await page.setContent(htmlContent, {
            waitUntil: ['load', 'networkidle0'],
            timeout: CONFIG.timeout
        });
        
        // Generate PDF
        await page.pdf({
            path: outputPath,
            format: 'A4',
            margin: {
                top: '1in',
                right: '1in',
                bottom: '1in',
                left: '1in'
            },
            printBackground: true,
            preferCSSPageSize: false
        });
        
        return true;
        
    } catch (error) {
        throw new Error(`PDF conversion failed: ${error.message}`);
    } finally {
        if (browser) {
            await browser.close();
        }
    }
}

// Find all supported files recursively
async function findFiles(dir) {
    const files = [];
    
    async function scan(currentDir) {
        try {
            const entries = await fs.readdir(currentDir, { withFileTypes: true });
            
            for (const entry of entries) {
                const fullPath = path.join(currentDir, entry.name);
                
                if (entry.isDirectory()) {
                    await scan(fullPath);
                } else if (entry.isFile()) {
                    const ext = path.extname(entry.name).toLowerCase();
                    if (CONFIG.supportedExtensions.includes(ext)) {
                        files.push({
                            inputPath: fullPath,
                            relativePath: path.relative(dir, fullPath),
                            outputPath: path.join(CONFIG.outputDir, path.relative(dir, fullPath).replace(ext, '.pdf')),
                            extension: ext
                        });
                    }
                }
            }
        } catch (error) {
            log('ERROR', `Error scanning directory ${currentDir}: ${error.message}`);
        }
    }
    
    await scan(dir);
    return files;
}

// Process files in batches
async function processBatch(files, startIndex, batchSize) {
    const batch = files.slice(startIndex, startIndex + batchSize);
    const results = [];
    
    for (const file of batch) {
        try {
            // Ensure output directory exists
            await fs.mkdir(path.dirname(file.outputPath), { recursive: true });
            
            // Check if already exists
            try {
                await fs.access(file.outputPath);
                log('INFO', `Skipping ${file.relativePath} - already exists`);
                results.push({ file, skipped: true });
                continue;
            } catch {
                // File doesn't exist, proceed
            }
            
            // Convert file
            await convertToPDF(file.inputPath, file.outputPath);
            
            // Verify output
            const stats = await fs.stat(file.outputPath);
            const sizeKB = Math.round(stats.size / 1024);
            
            log('SUCCESS', `Converted ${file.relativePath} (${sizeKB} KB)`);
            results.push({ file, converted: true, size: sizeKB });
            
        } catch (error) {
            log('ERROR', `Failed to convert ${file.relativePath}: ${error.message}`);
            results.push({ file, error: error.message });
        }
    }
    
    return results;
}

// Main conversion function
async function main() {
    try {
        log('INFO', 'üöÄ Starting batch PDF conversion for generated documents...');
        
        // Ensure output directory exists
        await fs.mkdir(CONFIG.outputDir, { recursive: true });
        
        // Find all files
        log('INFO', `üìÇ Scanning ${CONFIG.inputDir}...`);
        const files = await findFiles(CONFIG.inputDir);
        
        if (files.length === 0) {
            log('WARN', '‚ö†Ô∏è No supported files found');
            return;
        }
        
        log('INFO', `üìÑ Found ${files.length} files to convert`);
        
        // Show breakdown by file type
        const breakdown = files.reduce((acc, file) => {
            acc[file.extension] = (acc[file.extension] || 0) + 1;
            return acc;
        }, {});
        
        Object.entries(breakdown).forEach(([ext, count]) => {
            log('INFO', `   ${ext}: ${count} files`);
        });
        
        // Process files in batches
        const results = {
            total: files.length,
            converted: 0,
            skipped: 0,
            failed: 0,
            totalSize: 0
        };
        
        const startTime = Date.now();
        
        for (let i = 0; i < files.length; i += CONFIG.concurrency) {
            const batchResults = await processBatch(files, i, CONFIG.concurrency);
            
            batchResults.forEach(result => {
                if (result.skipped) {
                    results.skipped++;
                } else if (result.converted) {
                    results.converted++;
                    results.totalSize += result.size || 0;
                } else {
                    results.failed++;
                }
            });
            
            // Progress update
            const processed = Math.min(i + CONFIG.concurrency, files.length);
            const percentage = ((processed / files.length) * 100).toFixed(1);
            log('INFO', `üìä Progress: ${processed}/${files.length} (${percentage}%)`);
        }
        
        // Final summary
        const duration = ((Date.now() - startTime) / 1000).toFixed(1);
        const avgSize = results.converted > 0 ? Math.round(results.totalSize / results.converted) : 0;
        
        log('INFO', '\nüéâ === CONVERSION COMPLETE ===');
        log('INFO', `üìä Total files: ${results.total}`);
        log('INFO', `‚úÖ Converted: ${results.converted}`);
        log('INFO', `‚è≠Ô∏è Skipped: ${results.skipped}`);
        log('INFO', `‚ùå Failed: ${results.failed}`);
        log('INFO', `üìà Success rate: ${((results.converted / results.total) * 100).toFixed(1)}%`);
        log('INFO', `üìÅ Total size: ${Math.round(results.totalSize / 1024)} MB`);
        log('INFO', `üìÑ Average size: ${avgSize} KB`);
        log('INFO', `‚è±Ô∏è Duration: ${duration}s`);
        log('INFO', `üìÇ Output: ${CONFIG.outputDir}`);
        
        if (results.converted > 0) {
            log('SUCCESS', `üéâ Successfully converted ${results.converted} files to PDF!`);
        }
        
    } catch (error) {
        log('ERROR', `‚ùå Batch conversion failed: ${error.message}`);
        console.error(error);
        process.exit(1);
    }
}

// Run main function
main().catch(error => {
    console.error('‚ùå Unexpected error:', error);
    process.exit(1);
});
